package CS121;

public class ArrayPractice {

	public static void main(String[] args) {
		String [] name = new String [100];
		double [] nums = {23,45,19.25,3.14,2.71};
		name[0] = "Jonah";
		System.out.println("the value in element 0 is " + name[0]);
		System.out.println("the value in element 98 is " + name[98]);
		System.out.println("the length of the array is " + name.length);
		System.out.println("the length of nums array is " + nums.length);
		System.out.println("the value in element 4 is " + nums[4]);
				
	}

}
